package com.yqc.dynamic

/**
 * Created by yangqc on 2017/5/8.
 * 针对变量命名的最佳实践
 */
class VariableBestPractice {
    static void main(String[] args) {
        def aInt = 100
        println(aInt)

        def bFloat = 100.10
        println(bFloat)

        def cDouble = 100.101
        println(cDouble)

        def dString = "Hello,World!"
        println(dString)
    }
}
